"""### Phase 3.3: Advanced Robust Modeling (Parannettu yhdistelmämalli)
Perusmallien jälkeen kehitämme robustimman, rahoitusteorian erityispiirteet huomioivan mallinnusputken. Tässä yhdistämme viisi edistynyttä tekniikkaa:
1. **Logaritmiset tuotot:** Muunnamme %Chg asymmetrisestä symmetriseksi logaritmiseksi tuotoksi.
2. **Uudet piirteet:** Laskemme osakkeen ylimyytyä/yliostettua tilaa kuvaavan **RSI-indikaattorin** (Relative Strength Index) sekä **sisäisen markkinatrendin** (kaikkien 5 yhtiön keskimääräinen tuotto edellisellä viikolla).
3. **Winsorisaatio:** Leikkaamme kohdemuuttujan pahimmat 1% ääriarvot (fat tails) pois, jotteivät yksittäiset markkinahäiriöt dominoi mallin oppimista.
4. **Huber Loss -häviöfunktio:** Käytämme XGBoostissa ja lineaarisessa regressiossa Huber-virhefunktiota, joka ei rankaise algoritmeja liikaa satunnaisesta kohinasta (outliereista) toisin kuin perinteinen neliövirhe (MSE).
"""

