# --- APUFUNKTIO TULOSTEN LASKENTAAN ---
def evaluate_model(name, y_true, y_pred):
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    mae = mean_absolute_error(y_true, y_pred)
    r2 = r2_score(y_true, y_pred)

    # Suunnan osumatarkkuus (Directional Accuracy): Olivatko molemmat plussalla tai molemmat miinuksella?
    dir_acc = np.mean(np.sign(y_true) == np.sign(y_pred)) * 100

    print(f"--- {name} ---")
    print(f"RMSE (Pienempi on parempi): {rmse:.4f}")
    print(f"MAE  (Pienempi on parempi): {mae:.4f}")
    print(f"R²   (Isompi on parempi)  : {r2:.4f}")
    print(f"Suunnan osumatarkkuus:      {dir_acc:.1f} %\n")

# ==========================================
# MALLI 1: Baseline (Naïve Forecast)
# ==========================================
