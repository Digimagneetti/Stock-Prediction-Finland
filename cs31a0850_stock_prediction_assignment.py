# Logaritmiset tuotot (turvallinen muunnos: ln(1 + x))
df_adv['log_return'] = np.log1p(df_adv['%Chg'])
# Kohdemuuttujan siirto logaritmisena
df_adv['target_log_return'] = df_adv.groupby('Company')['log_return'].shift(-1)

# 2. Sisäinen markkinatrendi (keskiarvo koko paneelista tietyllä viikolla)
df_adv['market_avg_log_return'] = df_adv.groupby('Date_parsed')['log_return'].transform('mean')
df_adv['market_avg_lag1'] = df_adv.groupby('Company')['market_avg_log_return'].shift(1)

from indicators import calculate_rsi

df_adv['RSI_4'] = df_adv.groupby('Company')['Close'].transform(lambda x: calculate_rsi(x, periods=4))

# Poistetaan puuttuvat arvot uusien laskentojen jäljiltä
df_adv = df_adv.dropna().reset_index(drop=True)

# 4. Ääriarvojen leikkaaminen (Winsorisaatio kohdemuuttujalle)
lower_bound = df_adv['target_log_return'].quantile(0.01)
upper_bound = df_adv['target_log_return'].quantile(0.99)
df_adv['target_log_return'] = df_adv['target_log_return'].clip(lower=lower_bound, upper=upper_bound)

# 5. Datan jako ja ominaisuuksien valinta
train_mask_adv = df_adv['Date_parsed'] < '2025-01-01'
test_mask_adv = df_adv['Date_parsed'] >= '2025-01-01'

